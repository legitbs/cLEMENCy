//clang complains and ignores it although docs say it should be fine
void SetupClemencyNFO() //__attribute__ ((section ("nfo")))
{
    unsigned int i;
    for(i = NFO_MEMORY & 0xfffffc00; i <= NFO_MEMORY_END; i+= 1024)
		SetMemoryProtection(i, MEMORY_PROTECTION_EXECUTE);

    NFOData = malloc(sizeof(clemency_nfo)*sizeof(short));
    NFODataLen = Convert_8_to_9(NFOData, clemency_nfo, sizeof(clemency_nfo));
}

unsigned short ClemencyNFO_Read9(unsigned int Location) //__attribute__ ((section ("nfo")))
{
    //if we have data then return it
    if((Location & IO_MASK) != NFO_MEMORY)
        return 0;

    //within our area?
    Location &= ~IO_MASK;
    if(Location > NFODataLen)
        return 0;

    //return data
    return NFOData[Location];
}

unsigned int ClemencyNFO_Protection(unsigned int Location, unsigned int Protection)
{
    //yes, I could embed this into the memory area however this more looks like it was worked in

    //if not us then whatever is asked
    if((Location & IO_MASK) != NFO_MEMORY)
        return Protection;

    //force execute
    return MEMORY_PROTECTION_EXECUTE;
}
